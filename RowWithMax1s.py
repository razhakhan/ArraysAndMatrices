class Solution:

    def rowWithMax1s(self,arr, n, m):
        index=m-1
        maxrow=0
        for i in range(n):
            flag=False
            while(index>=0 and arr[i][index]==1):
                flag=True
                index-=1
            if(flag):
                maxrow=i
        if(maxrow==0 and arr[0][m-1]==0):
            return -1
        return maxrow
                

